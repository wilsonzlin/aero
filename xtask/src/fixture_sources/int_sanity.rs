//! The original NASM source (`tests/fixtures/boot/int_sanity.asm`) is kept in-repo as
//! documentation, but CI/agents are not expected to have an assembler installed.
//!
//! Behavior (see the `.asm` for readable reference):
//!   - `int 10h` teletype: prints `A`
//!   - `int 15h` `E820h` (`SMAP`): writes the first entry to `0000:0600`
//!   - `int 16h` read key: stores `AX` at `0000:0510`
//!   - `int 13h` read sector: reads CHS `(0,0,2)` into `0000:0700` and stores the
//!     first byte at `0000:0520`
//!   - Writes signature `OK` to `0000:0530`, then halts
